    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!validateInput()){
            return;
        }
        try{
            String userId=jcReceptionistId.getSelectedItem().toString();
            Boolean result=ReceptionistDAO.updatePassword(userId, passwordString);
            if(result){
                JOptionPane.showMessageDialog(null, "Password Updated Successfully!");
                clearText();
                jcReceptionistId.setSelectedIndex(0);
                return;
            }
            JOptionPane.showMessageDialog(null, "Password not Updated.");
            clearText();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error", "Error!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

