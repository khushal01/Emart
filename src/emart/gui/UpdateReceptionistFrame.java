    private void loadReceptionistId() {
        try{
        map=ReceptionistDAO.getAllReceptionistId();
        if(map.isEmpty()){
            JOptionPane.showMessageDialog(null,"No Receptionist Present");
            return;
        }
        jButton1.setEnabled(true);
        Set<String> set=map.keySet();
        for(String id:set){
            jcReceptionistId.addItem(id);
        }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error", "Error!!", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
    public boolean validateInput(){
        char pwd[]=password.getPassword();
        char rePwd[]=rePassword.getPassword();
        if(pwd.length<4 || rePwd.length<4){
            JOptionPane.showMessageDialog(null,"Password must be of 4 characters");
            return false;
        }
        passwordString=String.valueOf(pwd);
        String rePasswordString=String.valueOf(rePwd);
        if(!passwordString.equals(rePasswordString)){
            JOptionPane.showMessageDialog(null,"Password donot Match");
            return false;
        }
        return true;
    }

    private void clearText() {
        password.setText("");
        rePassword.setText("");
    }
