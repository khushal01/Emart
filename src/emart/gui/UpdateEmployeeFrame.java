    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
            try{
                String empId=jComboBox1.getSelectedItem().toString();
                EmployeePojo e=EmployeeDAO.findByEmpId(empId);
                jTextField1.setText(e.getEmpName());
                jTextField2.setText(String.valueOf(e.getSalary()));
                jComboBox2.setSelectedItem(e.getJob());
            }
            catch(SQLException se){
                JOptionPane.showMessageDialog(null, "Error in the Database", "Databse Error!!", JOptionPane.ERROR_MESSAGE);
                   se.printStackTrace();
                   return;
            }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
            if(!validateInputs()){
                JOptionPane.showMessageDialog(null, "Please Input all the Fields", "Incomplete Input!!", JOptionPane.ERROR_MESSAGE);
            }
            else{
                try{
                    EmployeePojo e= new EmployeePojo();
                    e.setEmpId(jComboBox1.getSelectedItem().toString());
                    e.setEmpName(jTextField1.getText());
                    e.setJob(jComboBox2.getSelectedItem().toString());
                    e.setSalary(Double.parseDouble(jTextField2.getText()));
                    
                    boolean result=EmployeeDAO.updateEmployee(e);
                    
                    if(!result){
                        JOptionPane.showMessageDialog(null, "Employee cannot be updated", "Updation Error!!", JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Employee details updated successfully", "Updated Successfully!!", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Error in the Database", "Databse Error!!", JOptionPane.ERROR_MESSAGE);
                   se.printStackTrace();
                   return;
        }
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

