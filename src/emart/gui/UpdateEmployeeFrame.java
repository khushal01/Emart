    private void getEmployees() {
            
        try{
            jComboBox1.removeAll();
            List<String> allEmpId=EmployeeDAO.getAllEmpId();
            
            for(String id:allEmpId){
                jComboBox1.addItem(id);
            }
        }
        catch(SQLException se){
            JOptionPane.showMessageDialog(null, "Error in the Database", "Databse Error!!", JOptionPane.ERROR_MESSAGE);
                   se.printStackTrace();
                   return;
        }
    }

    private boolean validateInputs() {
        
            return !(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty());
    }

    
    
